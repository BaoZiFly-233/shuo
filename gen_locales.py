# ── Generate all locale files ───────────────────────────────────────────
for lang in LANGS:
    data = {}
    # UI strings
    for k, v in UI[lang].items():
        data[k] = v
    # about.license
    data["about.license"] = LICENSE[lang]
    # about.title
    data["about.title"] = UI[lang]["about.title"]
    # asr_lang.* keys
    data["asr_lang.auto"] = AUTO[lang]
    for code in LANGS:
        data[f"asr_lang.{code}"] = NAMES[lang][code]
    # Write
    path = OUT / f"{lang}.json"
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, indent=4, ensure_ascii=False)
        f.write("\n")
    print(f"  {path.name}: {len(data)} keys")

print(f"\nDone: {len(LANGS)} locale files generated.")
